# Data Types :- A data type tells what kind of value a variable holds.
# Primarily there are following data types in python.
# 1. Integers
# 2. Floating point numbers
# 3. Strings
# 4. Booleans
# 5. None
# Python is a fantastic language that automatically identifies the type of data for us.

# type casting-means converting one data type to another.
a = 10
print(type(a))
b = str(a)
print(type(b))

c = "10"
print(type(c))
print(type(int(c)))


d = '''hello'''
print(type(d))


k = 10
print(type(k))
m = str(k)
print(type(m))